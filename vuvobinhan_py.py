def tron_mang(a, b):
    mang_moi = []
    i = 0
    # Lay do dai nho nhat cua 2 mang de cong cac phan tu tuong ung
    min_len = min(len(a), len(b))

    # Buoc 1: Cong cac phan tu tuong ung
    while i < min_len:
        mang_moi.append(a[i] + b[i])
        i += 1

    # Buoc 2: Xu ly phan con du cua mang dai hon
    # Neu mang a dai hon
    if len(a) > len(b):
        phan_du = a[i:] # Lay cac phan tu con lai cua a
        tong_phan_du = sum(phan_du)
        mang_moi.append(tong_phan_du)

    # Neu mang b dai hon
    elif len(b) > len(a):
        phan_du = b[i:] # Lay cac phan tu con lai cua b
        tong_phan_du = sum(phan_du)
        mang_moi.append(tong_phan_du)

    return mang_moi

