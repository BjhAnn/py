import datetime

# a. Xây dựng cấu trúc SINHVIEN
class SinhVien:
    def __init__(self, ma_sv, ten, nam_sinh, diem_tb):
        self.ma_sv = ma_sv[:10]      # Tối đa 10 ký tự
        self.ten = ten[:20]          # Tối đa 20 ký tự
        self.nam_sinh = int(nam_sinh)
        self.diem_tb = float(diem_tb)

    def __str__(self):
        return f"MSV: {self.ma_sv} | Tên: {self.ten} | Năm sinh: {self.nam_sinh} | ĐTB: {self.diem_tb}"

# b. Hàm nhập danh sách n sinh viên
def nhap_danh_sach_sv(n):
    danh_sach = []
    for i in range(n):
        print(f"\nNhập thông tin sinh viên thứ {i+1}:")
        ma = input("Mã SV (10 ký tự): ")
        ten = input("Tên SV (tối đa 20 ký tự): ")
        nam = int(input("Năm sinh: "))
        diem = float(input("Điểm trung bình: "))
        sv = SinhVien(ma, ten, nam, diem)
        danh_sach.append(sv)
    return danh_sach

# c. Đếm sinh viên đủ điều kiện lên lớp (ĐTB >= 5)
def dem_sv_len_lop(ds):
    count = sum(1 for sv in ds if sv.diem_tb >= 5)
    print(f"\nSố sinh viên đủ điều kiện lên lớp: {count}")
    return count

# d. Xuất các sinh viên đủ 20 tuổi
def xuat_sv_20_tuoi(ds):
    nam_hien_tai = datetime.datetime.now().year
    print("\nDanh sách sinh viên đủ 20 tuổi:")
    found = False
    for sv in ds:
        if (nam_hien_tai - sv.nam_sinh) == 20:
            print(sv)
            found = True
    if not found: print("Không có sinh viên nào 20 tuổi.")

# e. Đếm sinh viên hệ Đại học (Mã chứa 'DH' ở vị trí 2, 3)
# Lưu ý: Vị trí 2,3 trong chuỗi (tính từ 0) là index 2 và 3
def dem_sv_he_dh(ds):
    count = 0
    for sv in ds:
        # Kiểm tra nếu chuỗi con từ index 2 đến 4 là "DH"
        if len(sv.ma_sv) >= 4 and sv.ma_sv[2:4].upper() == "DH":
            count += 1
    print(f"Số sinh viên hệ Đại học: {count}")

# f. Đếm sinh viên tên "Lan"
def dem_ten_lan(ds):
    # Lấy phần tên cuối cùng trong chuỗi họ tên
    count = 0
    for sv in ds:
        ten_tach_ra = sv.ten.split()[-1].lower()
        if ten_tach_ra == "lan":
            count += 1
    print(f"Số sinh viên tên Lan: {count}")

# g. Đếm sinh viên họ "Phan"
def dem_ho_phan(ds):
    count = 0
    for sv in ds:
        ho_tach_ra = sv.ten.split()[0].lower()
        if ho_tach_ra == "phan":
            count += 1
    print(f"Số sinh viên họ Phan: {count}")

