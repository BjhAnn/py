import random

# --- CÁC HÀM XỬ LÝ (THEO ĐỀ BÀI) ---

def tao_ma_tran(m, n):
    """a. Tạo mảng a chứa các số nguyên ngẫu nhiên từ 1-100"""
    return [[random.randint(1, 100) for _ in range(n)] for _ in range(m)]

def xuat_dong_k(a, k):
    """b. Xuất các phần tử thuộc dòng k"""
    if 0 <= k < len(a):
        print(f"Dòng {k}: {a[k]}")
    else:
        print(f"Lỗi: Dòng {k} không tồn tại!")

def xuat_cot_k(a, k):
    """c. Xuất các phần tử thuộc cột k"""
    if 0 <= k < len(a[0]):
        cot = [a[i][k] for i in range(len(a))]
        print(f"Cột {k}: {cot}")
    else:
        print(f"Lỗi: Cột {k} không tồn tại!")

def dong_tong_max(a):
    """d. Tìm dòng có tổng lớn nhất"""
    max_val = -1
    index = -1
    for i, dong in enumerate(a):
        tong = sum(dong)
        if tong > max_val:
            max_val = tong
            index = i
    print(f"Dòng {index} có tổng lớn nhất là: {max_val}")

def cot_tich_min(a):
    """e. Tìm cột có tích nhỏ nhất"""
    m, n = len(a), len(a[0])
    min_tich = float('inf')
    index_cot = -1
    for j in range(n):
        tich = 1
        for i in range(m):
            tich *= a[i][j]
        if tich < min_tich:
            min_tich = tich
            index_cot = j
    print(f"Cột {index_cot} có tích nhỏ nhất là: {min_tich}")

def xuat_dong_chan_cot_le(a):
    """f. Xuất các phần tử thuộc dòng chẵn và cột lẻ"""
    print("Các phần tử (dòng chẵn, cột lẻ):")
    for i in range(0, len(a), 2):
        for j in range(1, len(a[0]), 2):
            print(f"a[{i}][{j}]={a[i][j]}", end="  ")
    print()

def tbc_chan_dong_le(a):
    """g. TBC các phần tử chẵn thuộc dòng lẻ"""
    ds = [x for i in range(1, len(a), 2) for x in a[i] if x % 2 == 0]
    kq = sum(ds)/len(ds) if ds else 0
    print(f"TBC phần tử chẵn ở dòng lẻ: {kq:.2f}")

def tinh_bien_va_noi_tai(a):
    """h. TBC biên và Trung bình tích không thuộc biên"""
    m, n = len(a), len(a[0])
    bien = []
    noi_tai = []
    for i in range(m):
        for j in range(n):
            if i == 0 or i == m-1 or j == 0 or j == n-1:
                bien.append(a[i][j])
            else:
                noi_tai.append(a[i][j])

    tbc_bien = sum(bien)/len(bien) if bien else 0

    tich = 1
    for x in noi_tai: tich *= x
    tb_tich = tich / len(noi_tai) if noi_tai else 0

    print(f"TBC phần tử biên: {tbc_bien:.2f}")
    print(f"Trung bình tích phần tử bên trong: {tb_tich:.2f}")

